void pattern7(int n) {
        for(int i=0; i<n;i++){ // 0 , 1 , 2 , 3  --> n=4
            // space
            for(int j=0; j< n-i-1;j++){ // i=0, j--> 0,1,2,
                cout<<" ";
            }
            
            // stars
            for(int j=0; j< 2*i+1;j++){ // i=0, j--> 0 , i=1 , j--> 0,1,2 , i=2 ,j--> 0,1,2,3 ,i=3 ,j--> 0,1,2,3,4,5,6,
                cout<<"*";
            }

            // space
            for(int j=0; j<n-i-1;j++){ // i=0, j--> 0,1,2
                cout<<" ";
            }
            cout << endl;

        }

    }
